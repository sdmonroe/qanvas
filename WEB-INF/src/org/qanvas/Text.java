package org.qanvas;

public class Text {

	public String content;
	public String propertyIri;
	boolean restrictToLabels; // whether or not to apply the rdfs:label
								// restriction to the property of the text
								// criteria

	public boolean restrictToLabels() {
		return restrictToLabels;
	}

	public void setRestrictToLabels(boolean restrictToLabels) {
		this.restrictToLabels = restrictToLabels;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String text) {
		this.content = text;
	}

	public String getPropertyIri() {
		return propertyIri;
	}

	public void setPropertyIri(String propertyIri) {
		this.propertyIri = propertyIri;
	}

}
